package semana2;

public class Ejercicio1 {

    public static void main(String[] args) {

        // Formato -> Definiendo directamente los valores del arreglo
        int[] a = {4, 8, 9, 6, 1, 2};

        // Iterar los elementos del arreglo
        for (int i = 0; i < a.length; i++) {
            System.out.println("a[" + i + "]=" + a[i]);
        }
    }
}
